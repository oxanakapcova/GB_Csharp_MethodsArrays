/*
Вася не любит английский язык, но каждый раз старается получить хотя 
бы четверку за четверть, чтобы оставаться ударником. В текущей 
четверти Вася заметил следующую закономерность: по нечетным дням 
месяца он получал тройки, а по четным – четверки. Так же он помнит, в 
какие дни он получал эти оценки. Поэтому он выписал на бумажке все эти 
дни для того, чтобы оценить, сколько у него троек и сколько четверок. 
Помогите Васе это сделать, расположив четные и нечетные числа в разных 
строчках. Вася может рассчитывать на оценку 4, если четверок не меньше,
чем троек.
Входные данные
В первой строке входного файла INPUT.TXT записано единственное число 
N – количество элементов целочисленного массива (1 ≤ N ≤ 100). Вторая 
строка содержит N чисел, представляющих заданный массив. Каждый элемент
массива – натуральное число от 1 до 31. Все элементы массива разделены 
