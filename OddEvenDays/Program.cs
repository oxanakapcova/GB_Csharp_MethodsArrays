рассчитывать на четверку и «NO» в противном случае. В каждой строчке 
числа следует выводить в том же порядке, в котором они идут во входных 
данных. При выводе числа отделяются пробелом.
*/
Console.WriteLine("Hello, Arrays && Methods!");
static int[] GetRandomArray(uint Length, int minValue, int maxValue)
{
    int[] yourarray = new int[Length];
    Random random = new Random();
    for (int i = 0; i < yourarray.Length; i++)
        yourarray[i] = random.Next(minValue, maxValue);
    return yourarray;
}
int[] month = GetRandomArray(19, 1, 32);
Console.WriteLine($"Your array is: [{string.Join(" ", month)}]");
int odd = 0; int even = 0;
for (int i = 0; i < month.Length; i++)
{
    if (month[i] % 2 != 0)
        Console.Write(month[i] + " ");
}
Console.WriteLine("\n=================================");//для визуального отделения
for (int k = 0; k < month.Length; k++)
{
    if (month[k] % 2 == 0)
        Console.Write(month[k] + " ");
}
for (int j = 0; j < month.Length; j++)
{
    if (month[j] % 2 != 0)
        odd++;
    else
        even++;
}
if (even >= odd)//из условия этот момент не до конца ясен
    Console.WriteLine("\nYes!");
else
    System.Console.WriteLine("\nNo.");


